int	ft_isalpha(int c)
{
	if (c < 0 || c > 255)
		return (0);
	if (((unsigned char)c >= 'A' && (unsigned char)c <= 'Z') ||
			((unsigned char)c >= 'a' && (unsigned char)c <= 'z'))
	{
		return (1);
	}
	return (0);
}
