char	*ft_strcpy(char *dst, const char *src)
{
	char *s;
	char *s1;

	s = (char*)src;
	s1 = dst;
	while (*s)
	{
		*dst = *s;
		dst++;
		s++;
	}
	*dst = '\0';
	return (s1);
}
